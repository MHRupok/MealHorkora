    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String meal_value = (String) jComboBox1.getSelectedItem();
        JOptionPane.showMessageDialog(null, meal_value);

        if (meal_value.equalsIgnoreCase("Regular Lunch") || meal_value.equalsIgnoreCase("Special Lunch")) {
            MH.serveLunch = 1;
            MH.serveDinner = 0;
        } else if (meal_value.equalsIgnoreCase("Regular Dinner") || meal_value.equalsIgnoreCase("Special Dinner")) {
            MH.serveDinner = 1;
            MH.serveLunch = 0;
        }

        //jTextField2.setText(meal_value);
    }//GEN-LAST:event_jButton2ActionPerformed

